import math

print(round(1.3))  # redondea al numero mas cercano
print(round(1.5))
print(round(1.9))

print(abs(-77))  # devuelve el numero positivo
print(abs(55))


print(math.ceil(1.1))  # redondea al numero entero para arriba
print(math.floor(1.999999))  # redondea al numero entero para aabajo
# print(math.isnan("23"))  # para verificar que el valor que le pasamos no es un numero
print(math.pow(10, 3))  # permite elevar un numero a la potencia de algo
print(math.sqrt(9))  # para sacar la raiz cuadrada del valor
